<h2>How do you issue a book ?</h2>
<p>First, look at the catalogue of books in the library, in the above list. You can check if it has been issued or not. If it has been issued, then you can put in a claim for the book. Else, you can request the book, by filling the request form below. If it is available, it will be
taken out of the library and put in the room of the lit secy Vraj Shah (241), where you can come and collect it at designated times.</p><br/>

<h2>When can you take the book ?</h2>
<p>The timings for issue of books are:<br/>
Saturdays - 10 AM - 11 AM<br/>
Sundays - 10 AM - 11 AM<br/>
Wednesday - 9 PM-10 PM<br/>
</p>

<p>Also, please carry your i-card. We don't want any person not from the hostel to be stealing books. Cooperation in this regard is highly appreciated. Also go through the library rules and policies to avoid any possible inconvienence in future.</p><br/>

