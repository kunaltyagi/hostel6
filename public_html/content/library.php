<h2>When and how do you return the book ?</h2>
<p>The time for which a book is issued is 1 week, if there is a claim on it, and 2 weeks, if there is none. The Lit secy will get in touch with you at the end of the period, when you will have to put the book back in the room of the lit secy.</p><br/>

<h2>The library rules and policies</h2>
<ol>
<li><p>A book can only be issued for 2 weeks, with no claims, and 1 week, if there is a claim.</p></li>
<li><p>You cannot claim a book you have already issued.</li>
<li><p>Requests for a particular book cannot be transferred to another. You will have to fill out a fresh form.</p></li>
<li><p><span class="blu"><b>Book Restocking fee</b></span> - If the book is misplaced or lost, the following rules apply,</p>
	<ol><li><p>If the book has been issued n times before the person issuing it, he has to pay a restocking fee of max(50, 80-5n)% of the current
	market rate of the book.</p></li>
    <li><p>For a second such default, the person is ineligible to issue any
	books form the hostel's library.</p></li></ol></li>
<li><p>Please carry your ID card as proof of identity while taking the book from the Lit Secy's room.</p></li></ol>


